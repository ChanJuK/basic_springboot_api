package io.kakaotask1.chanjukyung.exception;

public class Exception {
	

    private String msg;
    
    
    
	public Exception(String msg) {
		super();
		this.msg = msg;
	}

	
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}

    
    
    
    
}



